#include "libft.h"

int	ft_putstr(const char *str)
{
	int	i;

	i = 0;
	if (str == 0)
		return (write(1, "(null)", 6));
	while (str[i] != 0)
	{
		i += ft_putchr(str[i]);
	}
	return (i);
}
